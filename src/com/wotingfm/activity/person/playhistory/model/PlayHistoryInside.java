package com.wotingfm.activity.person.playhistory.model;

public class PlayHistoryInside {
	private String UserId;
	private String PlayImageUrl;
	private String PlayName;
	private String SessionId;
	private String PlayIntroduce;
	public String getUserId() {
		return UserId;
	}
	public void setUserId(String userId) {
		UserId = userId;
	}
	public String getPlayImageUrl() {
		return PlayImageUrl;
	}
	public void setPlayImageUrl(String playImageUrl) {
		PlayImageUrl = playImageUrl;
	}
	public String getPlayName() {
		return PlayName;
	}
	public void setPlayName(String playName) {
		PlayName = playName;
	}
	public String getSessionId() {
		return SessionId;
	}
	public void setSessionId(String sessionId) {
		SessionId = sessionId;
	}
	public String getPlayIntroduce() {
		return PlayIntroduce;
	}
	public void setPlayIntroduce(String playIntroduce) {
		PlayIntroduce = playIntroduce;
	}


}
